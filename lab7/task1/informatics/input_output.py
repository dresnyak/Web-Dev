# Problem C: Read two numbers and print them in reverse order
a = int(input())
b = int(input())
print(b, a)

# Problem D: Read three numbers and print their sum and product
a = int(input())
b = int(input())
c = int(input())
print(a + b + c)
print(a * b * c)

# Problem E: Read two numbers and swap them
a = int(input())
b = int(input())
a, b = b, a
print(a, b)
